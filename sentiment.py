stemmer = PorterStemmer()

def basic_clean(text: str) -> str:
    text = text.lower()
    text = re.sub(r"http\S+|www\S+", " ", text)
    text = re.sub(r"[^a-z\s]", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text

def lemmatize_with_spacy(text: str) -> str:
    doc = NLP(text)
    tokens = [
        token.lemma_.strip()
        for token in doc
        if token.lemma_.strip()
        and token.lemma_.strip() not in STOPWORDS_EN
        and len(token.lemma_.strip()) > 1
    ]
    return " ".join(tokens)

def stem_with_nltk(text: str) -> str:
    tokens = text.split()
    tokens = [t for t in tokens if t not in STOPWORDS_EN and len(t) > 1]
    tokens = [stemmer.stem(t) for t in tokens]
    return " ".join(tokens)

def preprocess_text(text: str) -> str:
    text = basic_clean(text)
    if not text:
        return ""

    if SPACY_OK:
        try:
            return lemmatize_with_spacy(text)
        except Exception:
            pass

    if CONFIG["USE_STEMMING_IF_SPACY_FAILS"]:
        return stem_with_nltk(text)

    tokens = [t for t in text.split() if t not in STOPWORDS_EN and len(t) > 1]
    return " ".join(tokens)

