def evaluate_distilbert(model, tokenizer, texts, y_true):
    # Clear GPU cache before evaluation to maximize available memory
    import gc
    import torch
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = model.to(device)
    model.eval()

    all_logits = []

    # Process texts in smaller batches during evaluation to prevent OOM
    # Reuse chunk_iterable function from CÉLULA 9
    batch_size = CONFIG["EVAL_BATCH_SIZE"]
    if batch_size == 0:
        batch_size = 1 # Ensure batch size is at least 1

    for batch_texts in chunk_iterable(texts, batch_size):
        enc = tokenizer(
            batch_texts,
            truncation=True,
            padding="max_length",
            max_length=CONFIG["MAX_TOKEN_LENGTH"],
            return_tensors="pt"
        )
        enc = {k: v.to(device) for k, v in enc.items()}

        with torch.no_grad():
            outputs = model(**enc)
            all_logits.append(outputs.logits.detach().cpu()) # Move logits to CPU to free GPU memory

    logits = torch.cat(all_logits, dim=0) # Concatenate all logits after processing all batches

    probs = torch.softmax(logits, dim=1)[:, 1].numpy()
    preds = np.argmax(logits.numpy(), axis=1)

    return preds, probs

def evaluate_mlp(model, vectorizer, texts, y_true):
    X_vec = vectorizer.transform(texts)
    preds = model.predict(X_vec)
    probs = model.predict_proba(X_vec)[:, 1]
    return preds, probs

