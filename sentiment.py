def chunk_iterable(lst, batch_size):
    for i in range(0, len(lst), batch_size):
        yield lst[i:i+batch_size]

def pseudo_label_texts(texts, batch_size=32, min_conf=0.80):
    labels = []
    scores = []

    for batch in chunk_iterable(texts, batch_size):
        short_batch = [t[:CONFIG["MAX_TEXT_CHARS_FOR_LABELING"]] for t in batch]
        preds = sentiment_pipe(short_batch, truncation=True)

        for p in preds:
            label_raw = p["label"].upper()
            score = float(p["score"])

            if score < min_conf:
                labels.append(None)
                scores.append(score)
                continue

            if "POS" in label_raw:
                labels.append(1)
            elif "NEG" in label_raw:
                labels.append(0)
            else:
                labels.append(None)

            scores.append(score)

    return labels, scores

