# Vamos gerar também frequência de termos e n-grams por categoria.

def get_top_terms(texts, ngram_range=(1,1), top_n=20):
    texts = [t for t in texts if isinstance(t, str) and t.strip()]
    if not texts:
        return pd.DataFrame(columns=["term", "count"])

    vec = CountVectorizer(
        stop_words=list(STOPWORDS_EN) + ["xxxx", "xx"], # Adicionando 'xxxx' e 'xx' às stopwords
        ngram_range=ngram_range,
        min_df=2
    )
    X = vec.fit_transform(texts)
    freqs = np.asarray(X.sum(axis=0)).ravel()
    terms = np.array(vec.get_feature_names_out())

    order = np.argsort(freqs)[::-1]
    top_terms = pd.DataFrame({
        "term": terms[order][:top_n],
        "count": freqs[order][:top_n]
    })
    return top_terms

all_top_tables = []

